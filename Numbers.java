public class Numbers {
    public static void main(String[] args) {
        System.out.println(0b10010010);//binary
        System.out.println(0x22);//hex
        System.out.println(15e-3f);//exp float
        System.out.println(2.0e10);//exp double
        System.out.println(1000000000000000000l);//long
        System.out.println(0127);//octal
        System.out.println();//
        System.out.println();//

    }
}
